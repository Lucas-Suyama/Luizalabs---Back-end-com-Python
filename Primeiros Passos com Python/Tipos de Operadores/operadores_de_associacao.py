# Operadores de Associação
# utilizados para verificar se um valor está contido em uma sequência
# retorna um valor booleano (True ou False)
## Exemplo:
curso = "Curso Python"
frutas = ["maçã", "banana", "laranja"]
saques = [100, 200, 300]

print("Python" in curso)

print("maçã" in frutas)

print("maçã" not in frutas)

print(200 in saques)
