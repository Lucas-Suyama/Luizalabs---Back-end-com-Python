# int para float
preco = 10
print(preco)

preco = float(preco)
print(preco)

preco = 10/2
print(preco)

#float para int
preco = 10.30
print(preco)

preco = int(preco)
print(preco)

# Conversão por divisão
preco = 10
print(preco)

print(preco/2)

#preserva o valor inteiro
print(preco//2)

#Numérico para string
preco = 1000
print(preco)

print(str(preco))

#String para numérico
idade = 28
preco = '1000'
print(preco)

print(int(idade))
print(float(preco))

# Erro de conversão
preco = 'python'
print(float(preco))