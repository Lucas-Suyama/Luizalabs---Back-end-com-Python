# Gerenciamento de Dependências com pipenv
# O pipenv é um gerenciador de pacotes para Python.
# Ele é usado para instalar, atualizar e desinstalar pacotes.
# Ele também é usado para gerenciar as dependências de um projeto.
# Isso significa que ele pode instalar as dependências de um projeto
# e garantir que elas estejam sempre atualizadas.
# Isso é muito útil para projetos que têm muitas dependências.

#Comandos:
#pip install pipenv - Instala o pipenv.
#pipenv install <nome_do_pacote> - Instala um pacote.
#pipenv install --upgrade <nome_do_pacote> - Atualiza um pacote.
#pipenv uninstall <nome_do_pacote> - Desinstala um pacote.
#pipenv list - Lista todos os pacotes instalados.
#pipenv search <nome_do_pacote> - Busca por um pacote.

