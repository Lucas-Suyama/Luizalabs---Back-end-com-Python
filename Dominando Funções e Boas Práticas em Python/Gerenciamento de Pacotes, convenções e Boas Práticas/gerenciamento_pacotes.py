# O que são pacotes?
# Pacotes são diretórios que contêm módulos Python.
# Eles são usados para organizar o código em um projeto.

#Papel do pip
# O pip é um gerenciador de pacotes para Python.
# Ele é usado para instalar, atualizar e desinstalar pacotes.

#Coamandos:
# pip install <nome_do_pacote> - Instala um pacote.
# pip install --upgrade <nome_do_pacote> - Atualiza um pacote.
# pip uninstall <nome_do_pacote> - Desinstala um pacote.
# pip list - Lista todos os pacotes instalados.
# pip search <nome_do_pacote> - Busca por um pacote.

